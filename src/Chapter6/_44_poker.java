/**
 * 面试题44：扑克牌顺子
 * LL今天心情特别好,因为他去买了一副扑克牌,发现里面居然有2个大王,2个小王(一副牌原本是54张^_^)...他随机从中抽出了5张牌,
 * 想测测自己的手气,看看能不能抽到顺子,如果抽到的话,他决定去买体育彩票,嘿嘿！！“红心A,黑桃3,小王,大王,方片5”,
 * “Oh My God!”不是顺子.....LL不高兴了,他想了想,决定大\小 王可以看成任何数字,并且A看作1,J为11,Q为12,K为13。
 * 上面的5张牌就可以变成“1,2,3,4,5”(大小王分别看作2和4),“So Lucky!”。LL决定去买体育彩票啦。
 * 现在,要求你使用这幅牌模拟上面的过程,然后告诉我们LL的运气如何。为了方便起见,你可以认为大小王是0。
 */

public class _44_poker {
	public static void main(String[] args) {
		int []num = {0,0,3,4,7};
		new _44_function().is_continuous(num);
	}
}
class _44_function {
	void is_continuous(int []num) {
		int count = 0;
		for (int i = 1; i < num.length; i++) {
			for (int j = 0; j < num.length - i; j++) {
				if (num[j] > num[j + 1]) 
					num[j] = num[j] + num[j + 1] - (num[j + 1] = num[j]);
			}
		}
		for (int i = 0; i < num.length; i++){
			if (num[i] != 0) break;
			count++;
		}
		int left = num[count], right;
		for (int i = count + 1; i < num.length; i++) {
			right = num[i];
			if (right - left == 0) {
				System.out.println("false.");
				return; 
			} else {
				if (right - left != 1) {
					if (right - left <= count + 1)
						count -= right - left;
					else {
						System.out.println("false.");
						return;
					}
				}
			}
			left = right;
		}
		System.out.println("true.");
		
	}
}
